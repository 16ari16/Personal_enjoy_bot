async def weather(update: Update, context: CallbackContext) -> None:
    if len(context.args) == 0:
        await update.message.reply_text("–£–∫–∞–∂–∏—Ç–µ –≥–æ—Ä–æ–¥ –¥–ª—è –ø–æ–∏—Å–∫–∞ –ø–æ–≥–æ–¥—ã, –Ω–∞–ø—Ä–∏–º–µ—Ä: /weather –ú–æ—Å–∫–≤–∞")
        return
    city = " ".join(context.args)
    try:
        response = requests.get(f'http://api.openweathermap.org/data/2.5/weather?q={city}&appid=your_weather_api_key&units=metric&lang=ru')
        weather_data = response.json()
        if weather_data.get("cod") != 200:
            await update.message.reply_text("–ì–æ—Ä–æ–¥ –Ω–µ –Ω–∞–π–¥–µ–Ω!")
        else:
            main = weather_data["main"]
            temp = main["temp"]
            description = weather_data["weather"][0]["description"]
            humidity = main["humidity"]
            wind_speed = weather_data["wind"]["speed"]
            weather_message = f"–ü–æ–≥–æ–¥–∞ –≤ {city}:\n–¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞: {temp}¬∞C\n–û–ø–∏—Å–∞–Ω–∏–µ: {description}\n–í–ª–∞–∂–Ω–æ—Å—Ç—å: {humidity}%\n–°–∫–æ—Ä–æ—Å—Ç—å –≤–µ—Ç—Ä–∞: {wind_speed} –º/—Å"
            await update.message.reply_text(weather_message)
    except Exception as e:
        logging.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ –ø–æ–≥–æ–¥—ã: {e}")
